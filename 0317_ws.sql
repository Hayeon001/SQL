--10-2번
SELECT EMPNO
	,ENAME
	,JOB
	,MGR
	,HIREDATE
	,SAL
	,COMM
	,DEPTNO
FROM EMP
WHERE ENAME LIKE '%S'
  AND JOB = 'SALESMAN'
  AND DEPTNO = 30;

