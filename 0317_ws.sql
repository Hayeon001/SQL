SELECT EMPNO
	,ENAME
	,JOB
	,MGR
	,HIREDATE
	,SAL
	,COMM
	,DEPTNO
FROM EMP
WHERE (DEPTNO = 20 OR DEPTNO = 30) AND SAL > 2000;

--10-4번
 SELECT EMPNO
