SELECT DEPTNO
	,ROUND(AVG(SAL), 0) AS AVG_SAL
	,MAX(SAL) AS MAX_SAL
	,MIN(SAL) AS MIN_SAL
	,COUNT(SAL) AS CNT
 FROM EMP E
 GROUP BY DEPTNO;

--(8-2)
SELECT JOB
	,COUNT(*)
 FROM EMP
  GROUP BY JOB
 HAVING COUNT(JOB) >= 3;

--(8-3)
SELECT TO_CHAR(HIREDATE, 'YYYY') AS HIRE_YEAR
	, DEPTNO
	, COUNT(*) AS CNT
FROM EMP
GROUP BY TO_CHAR(HIREDATE, 'YYYY'), DEPTNO;

