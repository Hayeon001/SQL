SELECT CASE WHEN COMM IS NULL THEN 'N' ELSE 'Y' END AS EXIST_COMM
	, COUNT(*) AS CNT
FROM EMP
GROUP BY CASE WHEN COMM IS NULL THEN 'N' ELSE 'Y' END;

--(9-5)
SELECT DEPTNO
	, TO_CHAR(HIREDATE, 'YYYY') AS HIRE_YEAR
	, COUNT(*) AS CNT
	, MAX(SAL) AS MAX_SAL
	, SUM(SAL) AS SUM_SAL
	, AVG(SAL) AS AVG_SAL
 FROM EMP
 GROUP BY ROLLUP (DEPTNO, TO_CHAR(HIREDATE, 'YYYY'));

